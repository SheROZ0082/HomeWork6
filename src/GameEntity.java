public class GameEntity {
    private int halth;
    private int Damage;

    public int getHalth() {
        return halth;
    }

    public void setHalth(int halth) {
        this.halth = halth;
    }

    public int getDamage() {
        return Damage;
    }

    public void setDamage(int damage) {
        Damage = damage;
    }
}
